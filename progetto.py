import networkx as nx
from matplotlib import pyplot as plt, lines
import matplotlib

def degree(day):
    dict_degree = dict(nx.degree(day))
    max_degree = max(dict_degree.values())
    min_degree = min(dict_degree.values())
    average_degree = sum(dict_degree.values())/len(dict_degree)
    return [dict_degree, max_degree, min_degree, average_degree]

def bet(day):
    dict_betweeness = dict(nx.betweenness_centrality(day))
    max_betweeness = max(dict_betweeness.values())
    min_betweeness = min(dict_betweeness.values())
    average_betweeness = sum(dict_betweeness.values())/len(dict_betweeness)
    return [dict_betweeness, max_betweeness, min_betweeness, average_betweeness]

def eig(day):
    dict_eigen = dict(nx.eigenvector_centrality(day))
    max_eigen = max(dict_eigen.values())
    min_eigen = min(dict_eigen.values())
    average_eigen = sum(dict_eigen.values())/len(dict_eigen)
    return [dict_eigen, max_eigen, min_eigen, average_eigen]

def bridges(day):
    ponti=list(nx.bridges(day))
    return ponti

def densità(day):
  density = nx.density(day)
  density_percentuale = round(density * 100, 2)
  return [density, density_percentuale]

def connessione(day):
  return nx.is_connected(day)

def giorno(file_giorno):
  day = nx.read_edgelist(file_giorno, data = False)
  formiche = day.number_of_nodes()
  interazioni = day.number_of_edges()
  print(f'Il numero di formiche che hanno interagito il {file_giorno[7]} è stato: {formiche}')
  print(f'''
Il numero totale di interazioni tra formiche diverse è stato: {interazioni}
Vuol dire che sono avvenute il {densità(day)[1]}% di interazioni possibili tra due formiche diverse
  ''')

  if nx.is_connected(day):
    print('Non ci sono stati gruppi isolati di formiche')
  else:
    print('Ci sono stati gruppi isolati di formiche')

  return day

def istogramma(centrality, nome_centrality):
  plt.figure(figsize=(6, 7))

  plt.hist(centrality[0].values(),
           color = 'skyblue'
          )

  plt.axvline(x = centrality[3],
              color = 'r',
              linestyle = 'dashed', linewidth = 1
             )

  plt.title(f'Distribuzione della {nome_centrality.lower()}', color = 'red', weight = 'bold')
  plt.xlabel(f'{nome_centrality}')
  plt.ylabel('Frequenza')
  plt.show()

def Graph_bridges(G, giorno):
  ponti=bridges(G)
  non_ponti=[]
  for edge in G.edges():
    if frozenset(edge) not in ponti:
        non_ponti.append(edge)

  pos = nx.spring_layout(G, seed=42)
  plt.style.use('ggplot')
  fig = plt.figure(figsize=(15,15))
  ax = fig.add_subplot()

  nx.draw_networkx_nodes(G,
                           pos,
                           alpha = 0.67,
                           node_size = 500,
                           node_color= 'black'
                           )

  nx.draw_networkx_edges(G,
                           pos,
                           edgelist=ponti,
                           style = '-', edge_color = 'red', width=8
                           )
  nx.draw_networkx_edges(G,
                           pos,
                           edgelist=non_ponti,
                           style = '-', edge_color = 'blue', width=1
                           )

  nx.draw_networkx_labels(G,
                          pos,
                          font_size=10, font_color = 'white'
                          )

  fig.suptitle('PONTI DEL GRAFO',
                 fontstyle='italic',fontweight = 'normal', fontsize =25 , color = 'red'
                 )
  ax.set_title(giorno,
                 fontsize = 15, color = 'orange', fontweight = 'bold'
                 )
  plt.show()

def matrice(G, giorno):
    M = nx.adjacency_matrix(G)
    fig = plt.figure(figsize=(8, 8))
    ax = fig.add_subplot()

    fig.add_artist(lines.Line2D([0.13, 0.9], [0.88, 0.11]))
    ax.matshow(M.todense())

    fig.suptitle('Matrice di Adiacenza',
                 fontfamily='serif', fontweight='bold', fontsize=20, color='red')

    ax.set_title(giorno,
                 fontstyle='italic', fontsize=15, color='orange', fontweight='bold')

    plt.show()

