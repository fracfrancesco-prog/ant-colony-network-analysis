def gruppi_di_giornate(day1_included, day2_included, day3_included):
    combined_graph = nx.Graph()
    if day1_included:
        g1 = nx.read_edgelist('ant_day1.edges', data=False)
        combined_graph.add_nodes_from(g1.nodes())
        combined_graph.add_edges_from(g1.edges())
    if day2_included:
        g2 = nx.read_edgelist('ant_day2.edges', data=False)
        combined_graph.add_nodes_from(g2.nodes())
        combined_graph.add_edges_from(g2.edges())
    if day3_included:
        g3 = nx.read_edgelist('ant_day3.edges', data=False)
        combined_graph.add_nodes_from(g3.nodes())
        combined_graph.add_edges_from(g3.edges())
    return combined_graph

